// 绘制正方体
const pathGenerator = function (points) {
  return "M" + points.map((p) => p.join(",")).join(" L") + " Z";
};

const cubeFaces = svg
  .selectAll(".face")
  .data(faces)
  .enter()
  .append("path")
  .attr("class", "face")
  .attr("d", pathGenerator)
  .attr("fill", (d, i) => faceColors[i]);

// 切换展开/收起状态
let isUnfolded = false;

d3.select("#toggle-btn").on("click", function () {
  isUnfolded = !isUnfolded;

  let first = cubeFaces
    .transition()
    .duration(1000)
    .attrTween("d", function (d, i) {
      const start = d;
      const end = isUnfolded
        ? unfoldedPositions[document.getElementById("number").value - 1].values[i]
        : faces[i];

      return function (t) {
        const interpolated = start.map((point, j) => {
          return [
            point[0] + (end[j][0] - point[0]) * t,
            point[1] + (end[j][1] - point[1]) * t,
          ];
        });
        return pathGenerator(interpolated);
      };
    });
  if (document.getElementById('number').value == 4) {
    /// 这里想执行第二次动画
    first.transition()
      .duration(1000)
      .attrTween("d", function (d, i) {
        const start = unfoldedPositions[document.getElementById("number").value - 1].values[i]
        const end = isUnfolded
          ? unfoldedPositions[document.getElementById("number").value - 1].values2[i]
          : faces[i];

        return function (t) {
          const interpolated = start.map((point, j) => {
            return [
              point[0] + (end[j][0] - point[0]) * t,
              point[1] + (end[j][1] - point[1]) * t,
            ];
          });
          return pathGenerator(interpolated);
        };
      })
      ;
  }


  // 更新按钮文本
  d3.select(this).text(isUnfolded ? "收起" : "展开/收起");
});

